## What is pydantic?

""" 
Pydantic is a data validation and settings management library for Python, based on Python type annotations. It allows you to define data models with type hints, and it automatically validates and parses input data to ensure it conforms to the specified types. Pydantic is commonly used in web development, particularly with frameworks like FastAPI, to handle request and response data in a type-safe manner. 
"""

